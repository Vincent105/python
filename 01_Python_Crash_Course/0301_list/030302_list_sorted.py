#使用函數sorted() 對列表暫時排序
cars = ['bmw', 'audi', 'toyota', 'subaru']

print("Here is the orginal list:")
print(cars)

print("Here is the sorted list:")
print(sorted(cars))

print("Here is the orginal list:")
print(cars)

print("Here is the reverse-sorted list:")
print(sorted(cars,reverse=True))

print("Here is the orginal list:")
print(cars)

